#include<iostream>
using namespace std;

int main() {
    // bitwise operators
    // & operator
    int a = 2^3;
    cout<<a<<endl;
    // | operator
    int e = 23|12;
    cout<<e<<endl;
    // ~ operator
    int d = ~5;
    cout<<d<<endl;
    // ^ operator
    int f = 23^12;
    cout<<f<<endl;
    // << operator
    int b = 2<<3;
    cout<<b<<endl;
    // >> operator
    int c = 18>>3;
    cout<<c<<endl;

    return 0;
}